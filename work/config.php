###a 初期設定ファイル ###
###i キー　で現在のカーソル位置に文字挿入###
###r キー　現在のカーソル位置から文字を上書き ###
### A　キー　現在のカーソル業の最後に文字追加　###
###コマンドモード ###
##x キー　1文字削除
## yy 1行コピー
## p　クリップボードの内容をカーソル行の下に張り付け
##dd 1行切り取り(または1行削除)

## grep "検索したい文字列" -rl パス　
　→ファイルの中身に　検索したい文字列が含まれているものをリスト表示


##find [検索対象フォルダのパス] -type f -name "*[検索したい文字列]*"
 ⇒ファイル名に検索したい文字列を含むファイルをリスト表示